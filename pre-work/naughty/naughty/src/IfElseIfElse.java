public class IfElseIfElse {
    public static void main (String args[]){
        int x = 0;
        // If Else If Else statment
        if (x < 0) {
            System.out.println("Negative number");
        } else if (x > 0) {
            System.out.println("Positive Number");
        } else {
            System.out.println("Zero");
        }
    }
}
